	@Override
	public void giveMoney(int i, Player []arr) {
		if(Hard_answer.Check_Answer(arr, i, Hard_answer.show_answer(i, arr)) == true) {
			if (King_area[arr[i].getPos()] == 0) {
				int x = arr[i].wallet + Bank.getAmount() + 2000;
				arr[i].setWallet(x);
				Bank.setMoney(-( Bank.getAmount() + 2000));
			}
		}
	}

	@Override
	public void takeMoney(int i, Player []arr) {
		if(Hard_answer.Check_Answer(arr, i, Hard_answer.show_answer(i, arr)) == false) {
			if(King_area[arr[i].getPos()] == 0) {
				int x = arr[i].wallet - Bank.getAmount() - 800;
				arr[i].setWallet(x);
				Bank.setMoney(Bank.getAmount() - 800);
			}
		}
	}

	@Override
	public void transfer(int i, int i0, Player []arr) {
		if(King_area[arr[i].getPos()] != 0) {
			if(Hard_answer.Check_Answer(arr, i, Hard_answer.show_answer(i, arr)) == true) {
				int x = arr[i].wallet - Bank.getAmount();
				arr[i].setWallet(x);
				int z = arr[i0].wallet + Bank.getAmount();
				arr[i0].setWallet(z);
			}else {
				int x = arr[i].wallet - (int)(Bank.getAmount() - Bank.getAmount()*0.3);
				arr[i].setWallet(x);
				int z = arr[i0].wallet + (int)(Bank.getAmount() - Bank.getAmount()*0.3);
				arr[i0].setWallet(z);
		    } 
		}
	}
	
