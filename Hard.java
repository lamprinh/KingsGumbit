	@Override
	public void giveMoney(Player p) {
		if(Hard_answer.show_answer() == true) {
			if (Game.Seat_check(p.getPos()) == false) {
				int x = p.wallet + Bank.getAmount() + 2000;
				p.setWallet(x);
				Bank.setMoney(-2000);
			}
		}
	}

	@Override
	public void takeMoney(Player p) {
		if(Hard_answer.show_answer() == false) {
			if(Game.Seat_check(p.getPos()) == false) {
				int x = p.wallet + Bank.getAmount() - 800;
				p.setWallet(x);
				Bank.setMoney(800);
			}
		}
	}

	@Override
	public void tranfer(Player p, Player p0) {
		if(Game.Seat_check(p.getPos()) == true) {
			if(Hard_answer.show_answer() == true) {
				int x = p.wallet - Bank.getAmount();
				p.setWallet(x);
				int z = p0.wallet + Bank.getAmount();
				p0.setWallet(z);
			}else {
				int x = p.wallet - (int)(Bank.getAmount() - Bank.getAmount()*0.3);
				p.setWallet(x);
				int z = p0.wallet + (int)(Bank.getAmount() - Bank.getAmount()*0.3);
				p0.setWallet(z);
		    } 
		}
	}
	
