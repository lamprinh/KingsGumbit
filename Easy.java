	@Override
	public void giveMoney(int i, Player[] arr) {
		if (Bank.King_area[arr[i].getPos()] == 9) {
			System.out.println("H perioxi den anikei se kanenan.");
			int a = -1 * Bank.getAmount();
			arr[i].setWallet(Bank.getAmount());
			Bank.setMoney(a);
			System.out.println("To portofoli tou " + arr[i].getCharacter() + " exei " + arr[i].getWallet() + " euro.");
			buyProperty(i, arr);
		} else {
			int x = Bank.King_area[arr[i].getPos()];
			System.out.println("H perioxi anikei ston " + arr[x].getCharacter());
			transferTrue(i, arr);
		}
	}

	@Override
	public void takeMoney(int i, Player[] arr) {
		if (Bank.King_area[arr[i].getPos()] == 9) {
			System.out.println("H perioxi den anikei se kanenan.");
			int a = Bank.getAmount();
			arr[i].setWallet(-Bank.getAmount());
			Bank.setMoney(a);
			System.out.println("To portofoli tou " + arr[i].getCharacter() + "meiothike kata " + Bank.getAmount()
					+ "euro kai pleon exei sto portofoli tou " + arr[i].getWallet() + " euro.");
		} else {
			int x = Bank.King_area[arr[i].getPos()];
			System.out.println("H perioxi anikei ston " + arr[x].getCharacter());
			transferFalse(i, arr);
		}
	}

